// Explique o resultado das expressões:

console.log("5" - 2);
//3
// O operador - (subtração) força a conversão da string 

console.log("5" + 2);
//52
// O "+" faz concatenação se houver string

console.log(true + 1);
//2
//O booleano true é convertido para o número 1

console.log(false + 10);
//10
//O booleano false é convertido para o número 0
